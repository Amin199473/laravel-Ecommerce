    }
    public function deleteAll()
    {
        $contacts = Contact::get();
        if (!$contacts->isEmpty()) {
            foreach ($contacts as $contact) {
                $contact->delete();
            }
            return redirect()->back()->with('success', 'All contacts deleted succesfully');
        } else {
            return redirect()->back()->with('failed', 'There is none contact for delete');
        }
    }
